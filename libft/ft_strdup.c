#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*res;
	unsigned int	i;

	i = 0;
	while (s[i])
		i++;
	res = (char*)malloc((i + 1) * sizeof(char));
	if (!res)
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = s[i];
		i++;
	}
	res[i] = 0;
	return (res);
}
